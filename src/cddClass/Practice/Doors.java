package cddClass.Practice;

public class Doors {
    private int width;
    private int height;
    private int thickness;


    public Doors(int width, int height, int thickness) {
        this.width = width;
        this.height = height;
        this.thickness = thickness;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getThickness() {
        return thickness;
    }
}
