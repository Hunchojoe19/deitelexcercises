package tddClass;

public class ProductCategory {
    private final String ELECTRONICS;
    private final String GROCERIES;
    private final String UTENSILS;
    private final String CLOTHING;

    public ProductCategory(String electronics, String groceries, String utensils, String clothing) {
        ELECTRONICS = electronics;
        GROCERIES = groceries;
        UTENSILS = utensils;
        CLOTHING = clothing;
    }
}
